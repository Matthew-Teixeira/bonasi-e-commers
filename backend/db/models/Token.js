const { Schema, model } = require("mongoose");

const tokenSchema = new Schema({
    userId: {
        type: Schema.Types.ObjectId,
        required: true,
        ref: "user",
    },
    token: {
        type: String,
        required: true
    },
    expiresAt: {
        type: Date,
        required: true,
    },
});

const Token = model("Token", tokenSchema);

module.exports = Token;